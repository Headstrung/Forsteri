function combineDate(data::Array{Array{Float64,2},1})
    #=


    =#

    # Find the minimum first index and use it as a base.
    minDate = Inf
    for i = 1 : length(data)
        if data[i][1] < minDate
            minDate = data[i][1]
        end
    end

    return minDate

end
